const mongoose = require("mongoose");

const StudentSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  gender: {
    type: String,
    required: true,
  },
  qualification: {
    type: String,
    required: true,
  },
  courseInterestedIn: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("Student", StudentSchema);
